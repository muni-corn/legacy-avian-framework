	public static void create() {
		if (cursorPosCallback != null) {
			cursorPosCallback.close();
		}
		glfwSetCursorPosCallback(AvianApp.getWindow(), cursorPosCallback = new GLFWCursorPosCallback() {

			@Override
			public void invoke(long window, double xpos, double ypos) {
				mouseX = (float) xpos;
				mouseY = (float) ypos;
				AvianApp.mouseMoved(mouseX, mouseY);
			}
		});
		if (charCallback != null) {
			charCallback.close();
		}
		glfwSetCharCallback(AvianApp.getWindow(), charCallback = new GLFWCharCallback() {
			@Override
			public void invoke(long window, int codepoint) {
				character = ((char) codepoint);
				AvianApp.keyTyped(character);
			}
		});
		if (keyCallback != null) {
			keyCallback.close();
		}
		// Setup a key callback. It will be called every time a key is pressed, repeated or released.
		glfwSetKeyCallback(AvianApp.getWindow(), keyCallback = new GLFWKeyCallback() {
			@Override
			public void invoke(long window, int k, int scancode, int action, int mods) {
				AvianInput.key = k;
				keyboardWindow = window;
				keyAction = action;
				keyScancode = scancode;
				keyMods = mods;
				switch (action) {
					case PRESS:
						AvianApp.keyPressed(k);
						break;
					case RELEASE:
						AvianApp.keyReleased(k);
						break;
					case REPEAT:
						AvianApp.keyRepeated(k);
						break;

				}
			}
		});
		if (mouseButtonCallback != null) {
			mouseButtonCallback.close();
		}
		glfwSetMouseButtonCallback(AvianApp.getWindow(), mouseButtonCallback = new GLFWMouseButtonCallback() {
			@Override
			public void invoke(long window, int b, int action, int mods) {
				mouseAction = action;
				mouseWindow = window;
				AvianInput.button = b;
				mouseMods = mods;

				switch (action) {
					case PRESS:
						AvianApp.mousePressed(b);
						break;
					case RELEASE:
						AvianApp.mouseReleased(b);
						break;
				}
			}

		});
		if (scrollCallback != null) {
			scrollCallback.close();
		}
		glfwSetScrollCallback(AvianApp.getWindow(), scrollCallback = new GLFWScrollCallback() {
			@Override
			public void invoke(long window, double xoffset, double yoffset) {
				scrollWindow = window;
				scroll = (float) yoffset;
				AvianApp.mouseScrolled((int) yoffset);
			}

		});
	}

